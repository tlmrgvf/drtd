package de.tlmrgvf.drtd.dsp.window;

public final class HannWindow extends Window {
    private static final long serialVersionUID = -8514830931505315465L;

    @Override
    public String getDisplayName() {
        return "Hann";
    }

    @Override
    public float[] calculateCoefficients(int n) {
        float[] ret = new float[n];
        float N = n - 1;

        for (int i = 0; i < n; ++i) {
            ret[i] = (float) Math.pow(Math.sin((Math.PI * i) / N), 2);
        }

        return ret;
    }
}
